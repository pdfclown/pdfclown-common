package org.pdfclown.common.build.internal.util_.annot;

import static java.lang.annotation.ElementType.TYPE;
import static java.lang.annotation.RetentionPolicy.CLASS;

import java.lang.annotation.Documented;
import java.lang.annotation.Retention;
import java.lang.annotation.Target;

/**
 * Indicates that the annotated type is effectively synchronized.
 * <p>
 * If multiple threads access the type concurrently, there is no need of external synchronization.
 * </p>
 *
 * @author Stefano Chizzolini
 */
@Documented
@Retention(CLASS)
@Target(TYPE)
public @interface ThreadSafe {
}
